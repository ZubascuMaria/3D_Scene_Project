glm::vec3 oznPosition(0.0f); // Current position
float oznSpeed = 0.01f; // Adjust the speed as needed
void renderOzn(gps::Shader shader) {

    // update model matrix for teapot
    oznmodel = glm::translate(model, oznPosition);
    // update normal matrix for teapot
    oznMatrix = glm::mat3(glm::inverseTranspose(view * oznmodel));

    if (pressedKeys[GLFW_KEY_Y]) {
        oznPosition.y += oznSpeed;
        // update model matrix for teapot
        oznmodel = glm::translate(model, oznPosition);
        // update normal matrix for teapot
        oznMatrix = glm::mat3(glm::inverseTranspose(view * oznmodel));
    }
    if (pressedKeys[GLFW_KEY_H]) {
        oznPosition.y -= oznSpeed;
        // update model matrix for teapot
        oznmodel = glm::translate(model, oznPosition);
        // update normal matrix for teapot
        oznMatrix = glm::mat3(glm::inverseTranspose(view * oznmodel));
    }
    if (pressedKeys[GLFW_KEY_G]) {
        oznPosition.x -= oznSpeed;
        // update model matrix for teapot
        oznmodel = glm::translate(model, oznPosition);
        // update normal matrix for teapot
        oznMatrix = glm::mat3(glm::inverseTranspose(view * oznmodel));
    }
    if (pressedKeys[GLFW_KEY_J]) {
        oznPosition.x += oznSpeed;
        // update model matrix for teapot
        oznmodel = glm::translate(model, oznPosition);
        // update normal matrix for teapot
        oznMatrix = glm::mat3(glm::inverseTranspose(view * oznmodel));
    }

    // select active shader program
    shader.useShaderProgram();

    //send ozn model matrix data to shader
    glUniformMatrix4fv(modelLoc, 1, GL_FALSE, glm::value_ptr(oznmodel));

    //send ozn normal matrix data to shader
    glUniformMatrix3fv(normalMatrixLoc, 1, GL_FALSE, glm::value_ptr(oznMatrix));

    // draw scena
    ozn.Draw(shader);
}




